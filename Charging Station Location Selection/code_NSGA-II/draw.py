def plot_pareto_front(cost_1, cost_2, Sat_1, Sat_2, save_dir=None, prefix="pareto", dpi=300):

    plt.rcParams['font.family'] = 'Times New Roman'
    plt.rcParams['mathtext.fontset'] = 'custom'  # 如果需要数学字体也匹配

    cost = cost_1 + cost_2
    Sat = Sat_1 + Sat_2
    Sat = [-i for i in Sat]
    # 归一化到[0,1]
    cost_min, cost_max = min(cost), max(cost)
    sat_min, sat_max = min(Sat), max(Sat)

    # 防止分母为零
    cost_range = cost_max - cost_min + 1e-6
    sat_range = sat_max - sat_min + 1e-6

    plt.figure(figsize=(10, 6))


    costs_1 = [(i - cost_min) / cost_range for i in cost_1]
    sats_1 = [(-i - sat_min) / sat_range for i in Sat_1]

    # 过滤 (0,0) 和 (1,1) 点（正确方式）
    filtered_pairs = [
        (c, s) for c, s in zip(costs_1, sats_1)
        if not ((abs(c) < 1e-6 and abs(s) < 1e-6) or (abs(c - 1) < 1e-6 and abs(s - 1) < 1e-6))
    ]
    costs_1_filtered = [c for c, s in filtered_pairs]
    sats_1_filtered = [s for c, s in filtered_pairs]

    plt.scatter(
        costs_1_filtered, sats_1_filtered,
        s=100,
        c='#d7191c',
        edgecolors='k',
        alpha=0.8,
        zorder=3,
        label='Pareto Front (1st)'
    )


    costs_2 = [(i - cost_min) / cost_range for i in cost_2]
    sats_2 = [(-i - sat_min) / sat_range for i in Sat_2]

    # 同上过滤逻辑
    filtered_pairs_2 = [
        (c, s) for c, s in zip(costs_2, sats_2)
        if not ((abs(c) < 1e-6 and abs(s) < 1e-6) or (abs(c - 1) < 1e-6 and abs(s - 1) < 1e-6))
    ]
    costs_2_filtered = [c for c, s in filtered_pairs_2]
    sats_2_filtered = [s for c, s in filtered_pairs_2]

    plt.scatter(
        costs_2_filtered, sats_2_filtered,
        s=100,
        facecolors='none',
        edgecolors='#2c7bb6',
        linewidths=1.5,
        alpha=0.8,
        zorder=3,
        label='Pareto Front (2nd)'
    )

    # 坐标轴标签和标题
    plt.xlabel('Economic Cost', fontsize=20)
    plt.xticks(fontsize=16)
    plt.ylabel('User Satisfaction', fontsize=20)
    plt.yticks(fontsize=16)
    plt.title('NSGA-II Pareto Front', fontsize=22)

    # 显式设置坐标轴
    plt.xlim(-0.05, 1.05)  # 扩展5%边距
    plt.ylim(-0.05, 1.05)

    plt.grid(True, linestyle='--', alpha=0.5)
    plt.tight_layout()
    plt.legend(fontsize=16)

    # 保存和显示逻辑
    if save_dir:
        os.makedirs(save_dir, exist_ok=True)
        pattern = os.path.join(save_dir, f"{prefix} _*.png")
        existing_files = glob.glob(pattern)
        max_num = 0
        for f in existing_files:
            match = re.search(rf"{prefix}_(\d{{3}})\.png$", f)
            if match:
                current_num = int(match.group(1))
                max_num = max(max_num, current_num)
        new_num = max_num + 1 if existing_files else 1
        filename = f"{prefix}_{new_num:03d}.png"
        save_path = os.path.join(save_dir, filename)
        plt.savefig(save_path, dpi=dpi, bbox_inches='tight')
        print(f"图表已保存至：{os.path.abspath(save_path)}")
    plt.show()
    plt.close()


plot_pareto_front(cost_1, cost_2, Sat_1, Sat_2, save_dir='./picture')
